num_1 = int(input('Digite um número: '))
num_2 = int(input('Digite um número: '))


if num_1 == 0:
    print('Valor inválido para entrada. Tente novamente')
elif num_1 > num_2:
    print(f'{num_1} é maior que {num_2}')
elif num_1 < num_2:
    print(f'{num_2} é maior que {num_1}')
else:
    print(f'{num_1} e {num_2} são iguais')
print('Obrigado')

   



    
    
