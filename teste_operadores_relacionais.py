# Variáveis

numero1 = 5 # int
numero2 = 3 # int

# Imprimir

print(numero1, '=', numero2, '?', numero1 == numero2)
print(numero1, '!=', numero2, '?', numero1 != numero2)
print(numero1, '>', numero2, '?', numero1 > numero2)
print(numero1, '<', numero2, '?', numero1 < numero2)
print(numero1, '>=', numero2, '?', numero1 >= numero2)
print(numero1, '<=', numero2, '?', numero1 <= numero2)

